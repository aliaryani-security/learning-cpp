#include <iostream>
// include cmath library for more functions
#include <cmath>
using namespace std;

int main () {
	// max() is used to find the highest value
	cout << max(5,17) << endl;
	
	// min() is used to find the lowest value
	cout << min (5,17) << endl;
	
	// from cmath library
	// square root
	cout << sqrt (64) << endl;
	// round
	cout << round (45.6) << endl;
	// logarithm
	cout << log (2) << endl;
	return 0;
}